// 14. Armstrong Number In Java
/**
 * loops
 */
public class loops {
    public static void main(String[] args) {
        Scanner in=new Scanner(System.in);
        System.out.print("number:");
        int num=in.nextInt();
        int power=0;
        int check=0;
        int temp=0;
        int count=num;
        while(count>0){
            count/=10;
            check++;
        }
        count=num;
        while(count>0){
            temp=count%10;
            System.out.println(temp+"*");
            power+=Math.pow(temp, check);
            System.out.println(power+"**");
            count/=10;
        }
    }
}








